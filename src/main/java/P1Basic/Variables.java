package P1Basic;

public class Variables {
    public static void main(String[] args) {
        int MyInt = 577;
        short MyShort = 233;
        byte MyByte = 100; // 124 коментрии два сплеша много коментири так /* открывешь */ закрываешь
        long MyLong = 1111L;

        double MyDouble = 2.2;
        float  MyFloat = 2.2f;

        char MyChar = '2';
        boolean MyBoolean = true;

        System.out.println(MyInt + MyShort + MyByte + MyLong + MyDouble + MyFloat + MyChar );

        System.out.println(MyBoolean);
    }
}
